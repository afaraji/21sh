#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	unsigned int i;

	if (s != NULL)
	{
		i = 0;
		while (i < ft_strlen(s))
		{
			write(fd, &s[i], 1);
			i++;
		}
	}
}
