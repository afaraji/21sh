#include "libft.h"

void	ft_putstr(char const *s)
{
	int i;
	int len;

	if (s != NULL)
	{
		len = 0;
		while (s[len])
			len++;
		i = 0;
		while (i < len)
		{
			write(1, &s[i], 1);
			i++;
		}
	}
}
